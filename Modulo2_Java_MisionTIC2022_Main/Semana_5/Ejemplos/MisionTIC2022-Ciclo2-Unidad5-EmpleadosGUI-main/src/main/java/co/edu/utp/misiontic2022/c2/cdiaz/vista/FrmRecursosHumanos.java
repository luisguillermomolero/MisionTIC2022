    private void cerrarAplicacion() throws HeadlessException {
        if (JOptionPane.showConfirmDialog(this, "Est치 seguro en salir?", getTitle(), JOptionPane.YES_NO_OPTION)
                == JOptionPane.YES_OPTION) {
            dispose();
        }
    }

    private void cargarInfoInicial() {
        try {
            // Cargar listado de empleados
            var employees = controller.findAllEmployees();
            empleadosTableModel = new EmpleadosTableModel(employees);
            tblEmpleados.setModel(empleadosTableModel);

            tblEmpleados.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            tblEmpleados.getSelectionModel().addListSelectionListener((e) -> {
                var row = tblEmpleados.getSelectedRow();
                if (row == -1) { // No hay filas seleccionadas
                    limpiarCampos();
                    btnEliminarEmpleado.setEnabled(false);
                } else {
                    var employee = empleadosTableModel.getEmployee(row);
                    cargarDatosEmpleado(employee);
                    btnEliminarEmpleado.setEnabled(true);
                }
            });

            // Listado de departamentos
            var departamentos = controller.findAllDepartments();
            cmbDepartamento.removeAllItems();
            departamentos.forEach(departamento -> {
                cmbDepartamento.addItem(departamento);
            });
            cmbDepartamento.setSelectedIndex(-1);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), getTitle(), JOptionPane.ERROR_MESSAGE);
        }
    }

    private void limpiarCampos() {
        tblEmpleados.clearSelection();

        txtId.setText("");
        txtNombre.setText("");
        txtCorreoElectronico.setText("");
        cmbDepartamento.setSelectedIndex(-1);
    }

    private void nuevoEmpleado() {
        limpiarCampos();

        txtNombre.requestFocus();
    }

    private void cargarDatosEmpleado(Employee employee) {
        txtId.setText(employee.getId().toString());
        txtNombre.setText(employee.getName());
        txtCorreoElectronico.setText(employee.getEmail());
        cmbDepartamento.setSelectedItem(employee.getDepartment());
    }

    private void guardarEmpleado() {
        // Validar datos obligatorios
        if (txtNombre.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "El campo nombre es obligatorio");
            txtNombre.requestFocus();
            return;
        }
        if (cmbDepartamento.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "El campo departamento es obligatorio");
            cmbDepartamento.requestFocus();
            return;
        }

        // Obtengo la informacion de pantalla
        var id = txtId.getText().trim();
        var nombre = txtNombre.getText().trim();
        var email = txtCorreoElectronico.getText().trim();
        var departamento = (Department) cmbDepartamento.getSelectedItem();

        // Envio a guardar a la base de datos
        try {
            if (id.isBlank()) {
                var employee = controller.createEmployee(nombre, email, departamento.getId());
                // Agregarlo a la tabla de empleados
                empleadosTableModel.addEmployee(employee);
            } else {
                var row = tblEmpleados.getSelectedRow();
                var employee = controller.updateEmployee(Integer.valueOf(id), nombre, email, departamento.getId());
                // Actualizar el renglon de la tabla empleados
                empleadosTableModel.setEmployee(row, employee);
            }

            // Confirmar guardado
            JOptionPane.showMessageDialog(this, "El empleado ha sido guardado exitosamente");
            limpiarCampos();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), getTitle(), JOptionPane.ERROR_MESSAGE);
        }
    }

    private void eliminarEmpleado() {
        if (JOptionPane.showConfirmDialog(this, "Est치 seguro en eliminar este empleado?",
                getTitle(), JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            // Obtengo el id el empleado
            var id = Integer.valueOf(txtId.getText());
            try {
                controller.deleteEmployee(id);
                empleadosTableModel.removeEmployee(tblEmpleados.getSelectedRow());
                JOptionPane.showMessageDialog(this, "El empleado ha sido eliminado exitosamente");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), getTitle(), JOptionPane.ERROR_MESSAGE);
            }
        }
    }

